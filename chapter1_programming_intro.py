
#### Chapter 1: What's Progamming? ################################################################

### Programs and Programming ######################################################################

# A program is a set of instructions.

# Defining new operations and combining them to do useful things is the heart 
# of programming.

# Computer science isn't computer programming. Thinking like a computer 
# scientist means more than being able to program a computer: it requires 
# thinking at multiple levels of abstraction.

# Computational thinking is a way humans solve problems; it isn't trying to 
# get humans to think like computers but use computers to solve our real-world 
# problems.
